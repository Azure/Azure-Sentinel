def main() -> None:
    """CLI entry point."""
    parser = argparse.ArgumentParser(
        description="Generate interactive ASIM Schema Browser HTML page."
    )
    script_dir = Path(__file__).parent

    parser.add_argument(
        '--fields-csv',
        type=Path,
        default=script_dir / 'asim_fields.csv',
        help='Path to asim_fields.csv (default: asim_fields.csv in script directory)',
    )
    parser.add_argument(
        '--parsers-csv',
        type=Path,
        default=script_dir / 'asim_parsers.csv',
        help='Path to asim_parsers.csv (default: asim_parsers.csv in script directory)',
    )
    parser.add_argument(
        '--output-dir',
        type=Path,
        default=script_dir,
        help='Output directory for asim-browser.html (default: script directory)',
    )
    parser.add_argument(
        '--docs-base-path',
        type=str,
        default='',
        help='Relative path prefix from asim-browser.html to entity doc pages '
             '(e.g. "Solutions Docs/" when the page is at repo root)',
    )
    parser.add_argument(
        '--index-url',
        type=str,
        default='',
        help='URL to the main index.html page for the navbar link',
    )
    parser.add_argument(
        '--link-extension',
        type=str,
        default='.html',
        help='File extension for entity page links (default: .html)',
    )

    args = parser.parse_args()

    # Normalize docs base path
    if args.docs_base_path and not args.docs_base_path.endswith('/'):
        args.docs_base_path += '/'

    generate_asim_browser(
        fields_csv=args.fields_csv,
        parsers_csv=args.parsers_csv,
        output_dir=args.output_dir,
        docs_base_path=args.docs_base_path,
        index_url=args.index_url,
        link_extension=args.link_extension,
    )


if __name__ == '__main__':
    main()
