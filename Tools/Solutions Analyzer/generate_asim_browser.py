def _generate_js() -> str:
    """Return the inline JavaScript for the page."""
    return """\
$(document).ready(function() {
    // Common DataTable config
    var dtDefaults = {
        pageLength: 50,
        lengthMenu: [[25, 50, 100, -1], [25, 50, 100, "All"]],
        order: [],
        autoWidth: false,
        deferRender: true,
        stateSave: false,
        orderCellsTop: true,
        dom: '<"row"<"col-sm-6"li><"col-sm-6"f>>rtip',
        language: {
            search: "Quick filter:",
            lengthMenu: "Show _MENU_ rows",
            info: "Showing _START_-_END_ of _TOTAL_",
            emptyTable: "No matching records",
        }
    };

    // Track initialized tables to avoid double-init
    var initializedTables = {};

    function initTable(tableId, extraOpts) {
        if (initializedTables[tableId]) return initializedTables[tableId];
        var $table = $('#' + tableId);
        if (!$table.length) return null;

        var opts = $.extend(true, {}, dtDefaults, extraOpts || {});
        opts.initComplete = function() {
            var api = this.api();
            $('#' + tableId + ' thead tr.filters th').each(function(i) {
                var column = api.column(i);
                if ($(this).data('filter') !== false) {
                    $('<input type="text" class="column-filter" placeholder="Filter...">')
                        .appendTo($(this).empty())
                        .on('keyup change clear', function() {
                            if (column.search() !== this.value) {
                                column.search(this.value).draw();
                            }
                            updateClearBtn(tableId, api);
                        });
                }
            });

            var clearBar = $(
                '<div class="clear-filters-bar">' +
                '<span class="clear-label">Filters are active</span>' +
                '<button class="btn-clear-all" title="Clear all filters">✕ Clear All Filters</button>' +
                '</div>'
            );
            clearBar.find('.btn-clear-all').on('click', function() {
                api.search('');
                api.columns().every(function() { this.search(''); });
                $('#' + tableId + ' thead tr.filters input.column-filter').val('');
                $(api.table().container()).find('.dataTables_filter input').val('');
                api.draw();
                clearBar.removeClass('visible');
            });
            $(api.table().container()).prepend(clearBar);

            $(api.table().container()).find('.dataTables_filter input').on('keyup change clear', function() {
                updateClearBtn(tableId, api);
            });
            updateClearBtn(tableId, api);
        };
        var dt = $table.DataTable(opts);
        initializedTables[tableId] = dt;
        return dt;
    }

    function updateClearBtn(tableId, api) {
        var hasFilter = !!api.search();
        if (!hasFilter) {
            api.columns().every(function() { if (this.search()) hasFilter = true; });
        }
        $(api.table().container()).find('.clear-filters-bar').toggleClass('visible', hasFilter);
    }

    // Initialize tables when their sub-tab becomes visible
    function initVisibleTable(schemaId) {
        var fieldsTableId = schemaId + '-fields-table';
        var parsersTableId = schemaId + '-parsers-table';

        if ($('#' + fieldsTableId).length && !initializedTables[fieldsTableId]) {
            // Find description column index dynamically (it has col-desc class in tbody)
            var descIdx = -1;
            $('#' + fieldsTableId + ' thead tr:first th').each(function(i) {
                if ($(this).text().trim() === 'Description') descIdx = i;
            });
            var colDefs = [];
            if (descIdx >= 0) colDefs.push({ targets: descIdx, className: 'col-desc' });
            initTable(fieldsTableId, {
                order: [[0, 'asc']],
                columnDefs: colDefs,
            });
        }
        if ($('#' + parsersTableId).length && !initializedTables[parsersTableId]) {
            initTable(parsersTableId, {
                order: [[2, 'asc'], [0, 'asc']],
            });
        }
    }

    // Schema tab switching
    $('button[data-bs-toggle="tab"]').on('shown.bs.tab', function(e) {
        var targetId = $(e.target).data('bs-target');
        // If this is a schema tab, init its tables
        if (targetId) {
            var schemaId = targetId.replace('#', '').replace('-pane', '');
            initVisibleTable(schemaId);
        }
        // Adjust DataTable columns on any tab show
        $.fn.dataTable.tables({ visible: true, api: true }).columns.adjust();

        // Update URL hash
        var id = $(e.target).attr('id');
        if (id) {
            // Build hash from schema tab + sub-tab
            var hash = id.replace('-tab', '');
            history.replaceState(null, '', '#' + hash);
        }
    });

    // Activate tab from URL hash
    function activateFromHash() {
        var hash = window.location.hash.replace('#', '');
        if (!hash) return;
        // Try as schema tab
        var tabBtn = document.getElementById(hash + '-tab');
        if (tabBtn) {
            bootstrap.Tab.getOrCreateInstance(tabBtn).show();
        }
    }

    // Initialize default (first visible) schema tables
    var firstSchemaPane = $('.schema-pane.show');
    if (firstSchemaPane.length) {
        var schemaId = firstSchemaPane.attr('id').replace('-pane', '');
        initVisibleTable(schemaId);
    }

    activateFromHash();
    $(window).on('hashchange', activateFromHash);
    window.addEventListener('pageshow', function(e) {
        if (e.persisted) activateFromHash();
    });

    // Click-to-filter
    $(document).on('click', 'table.dataTable tbody td', function(e) {
        if ($(e.target).closest('a, details, summary').length) return;
        var table = $(this).closest('table').DataTable();
        var colIdx = table.cell(this).index().column;
        var text = $(this).text().trim();
        if (!text || text === '—') return;
        var th = $($(this).closest('table').find('thead tr.filters th')[colIdx]);
        if (th.data('filter') === false) return;
        var input = th.find('input.column-filter');
        if (input.length) {
            input.val(text).trigger('change');
        }
    });

    // Hide loading
    $('#loading').fadeOut(200);
});
"""


