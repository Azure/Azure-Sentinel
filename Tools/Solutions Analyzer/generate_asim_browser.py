def load_fields_by_schema(fields_csv: Path) -> Dict[str, List[Dict[str, str]]]:
    """Load asim_fields.csv, group by schema.

    Returns dict: schema_name -> list of field dicts.
    Also includes 'Common' fields which will be merged into each schema tab.
    """
    by_schema: Dict[str, List[Dict[str, str]]] = defaultdict(list)
    for row in load_csv(fields_csv):
        schema = row.get('schema', '')
        if schema:
            by_schema[schema].append(row)
    return dict(by_schema)


def load_parsers_by_schema(parsers_csv: Path) -> Dict[str, List[Dict[str, str]]]:
    """Load asim_parsers.csv, group by schema.

    Returns dict: schema_name -> list of parser dicts, sorted by type then name.
    """
    by_schema: Dict[str, List[Dict[str, str]]] = defaultdict(list)
    for row in load_csv(parsers_csv):
        schema = row.get('schema', '')
        if schema and row.get('parser_type') != 'union':
            by_schema[schema].append(row)
    # Sort each schema's parsers alphabetically
    for schema in by_schema:
        by_schema[schema].sort(
            key=lambda p: p.get('parser_name', '').lower()
        )
    return dict(by_schema)


def load_logical_type_tooltips(logical_types_csv: Path) -> Dict[str, str]:
    """Load asim_logical_types.csv and build a name → tooltip description map.

    Strips markdown link syntax and HTML tags from descriptions to produce
    plain-text suitable for HTML title attributes.
    """
    tooltips: Dict[str, str] = {}
    for row in load_csv(logical_types_csv):
        name = row.get('name', '').strip()
        desc = row.get('description', '').strip()
        if not name:
            continue
        if not desc:
            tooltips[name] = ''
            continue
        # Strip markdown links [text](url) → text
        desc = re.sub(r'\[([^\]]+)\]\([^)]+\)', r'\1', desc)
        # Strip HTML tags (<br>, <br/>, etc.)
        desc = re.sub(r'<br\s*/?>', ' ', desc)
        desc = re.sub(r'<[^>]+>', '', desc)
        # Collapse whitespace
        desc = re.sub(r'\s+', ' ', desc).strip()
        tooltips[name] = desc
    return tooltips


